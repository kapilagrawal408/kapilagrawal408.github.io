package io.github.kapilagrawal408.myappsecond;

/**
 * This is a simple class that has method related to mathematical functions
 * @author Masai
 */
public class App {
	/**
	 * method to add two numbers and return their addition
	 * @param a The first value (the LHS operand for +)
	 * @param b The second value (the RHS operand for +)
	 * @return addition of the value of parameters a and b
	 */
	public static int add(int a, int b) {
		return (a + b);
	}
	
	/**
	 * method to subtract two numbers and return their subtraction
	 * @param a The first value (the LHS operand for -)
	 * @param b The second value (the RHS operand for -)
	 * @return subtraction of the value of parameters a and b
	 */
	public static int sub(int a, int b) {
		return (a - b);
	}
}
